local Blips = {}
local garageblip = true

local function createBlip(coords, label, color, scale, sprite, k)
    local blip = AddBlipForCoord(coords.x, coords.y, coords.z)
    SetBlipSprite(blip, sprite)
    SetBlipColour(blip, color)
    SetBlipAsShortRange(blip, true)
    SetBlipScale(blip, scale)
    BeginTextCommandSetBlipName("STRING")
    AddTextComponentString(label)
    EndTextCommandSetBlipName(blip)
    if not Blips[k] then
        Blips[k] = {}
    end
    table.insert(Blips[k], blip)
end

-- Create blips on resource start (for openatthefirstlogin = true)
Citizen.CreateThread(function()
    for k, v in pairs(AllBlips) do
        if v.openatthefirstlogin then
            for _, vv in pairs(v.blips) do
                createBlip(vv.coords, vv.label, vv.blipcolor, vv.blipsize, vv.blipid, k)
            end
        end
    end
end)

-- Handle blip toggle from radial menu
RegisterNetEvent('codem-supreme-radialmenu:openblip', function(data)
    if not data or not data.id then return end

    -- Close all blips
    if data.id == 'allblipclose' then
        for k, _ in pairs(Blips) do
            for _, vv in pairs(Blips[k]) do
                RemoveBlip(vv)
            end
            Blips[k] = nil
        end
        return
    end

    -- Open all blips
    if data.id == 'allblipopen' then
        -- First remove all existing blips
        for k, _ in pairs(Blips) do
            for _, vv in pairs(Blips[k]) do
                RemoveBlip(vv)
            end
            Blips[k] = nil
        end

        -- Then create all blips
        for kk, vv in pairs(AllBlips) do
            for _, vvv in pairs(vv.blips) do
                createBlip(vvv.coords, vvv.label, vvv.blipcolor, vvv.blipsize, vvv.blipid, kk)
            end
        end
        return
    end

    -- Toggle specific blip category
    if Blips[data.id] then
        -- Blips exist, remove them
        if data.id == 'garage' then
            ExecuteCommand('garageblip')
        else
            for _, v in pairs(Blips[data.id]) do
                RemoveBlip(v)
            end
            Blips[data.id] = nil
        end
    else
        -- Blips don't exist, create them
        if AllBlips[data.id] then
            if data.id == 'garage' then
                ExecuteCommand('garageblip')
                garageblip = true
            else
                for _, vv in pairs(AllBlips[data.id].blips) do
                    createBlip(vv.coords, vv.label, vv.blipcolor, vv.blipsize, vv.blipid, data.id)
                end
            end
        end
    end
end)
